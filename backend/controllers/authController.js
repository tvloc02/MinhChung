                message: `T√†i kho·∫£n ƒë√£ b·ªã kh√≥a do ƒëƒÉng nh·∫≠p sai qu√° nhi·ªÅu l·∫ßn. Th·ª≠ l·∫°i sau ${Math.ceil((user.lockUntil - Date.now()) / (1000 * 60))} ph√∫t.`
